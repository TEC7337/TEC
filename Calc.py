def add(x,y):
	return x+y

def subtract(x,y):
	return x-y

def multiply(x,y):
	return x*y

def divide(x,y):
	return x/y

def power(x,y):
	return x**y

def mod(x,y):
	return x%y

print(add(1,2))
print(subtract(1,2))
print(multiply(1,2))
print(divide(1,2))
print(power(1,2))
print(mod(1,2))